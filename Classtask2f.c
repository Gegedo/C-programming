// Assignment Operators

#include<stdio.h>
int main(){
    int a = 50;
    printf("Value of a is %d\n", a);

    a += 10;
    printf("Value of a is %d\n", a);

    a -= 10;
    printf("Value of a is %d\n", a);

    a *= 10;
    printf("Value of a is %d\n", a);

    a /= 10;
    printf("Value of a is %d\n", a);

    a %= 10;
    printf("Value of a is %d\n", a);

    return 0; 
   }